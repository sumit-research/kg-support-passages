
public class RankedDocumentSet {
	double document_score;
	int docId;
	String passage;
	
	
	
	public String getPassage() {
		return passage;
	}
	public void setPassage(String passage) {
		this.passage = passage;
	}
	public double getDocument_score() {
		return document_score;
	}
	public void setDocument_score(double document_score) {
		this.document_score = document_score;
	}
	public int getDocId() {
		return docId;
	}
	public void setDocId(int docId) {
		this.docId = docId;
	}
	
}
